//#region        Division
function Quotientenwert(Dividend:number, Divisor:number):number{
            return Dividend / Divisor;
}

                console.info('Function Quotentenwert: ' + Quotientenwert(10, 2));                             // Output: 5


//#endregion
//#region        Multiplikation
function Produktwert(Multiplikator:number, Multiplikand:number):number{
            return Multiplikator * Multiplikand;
}

                console.info('Function Produktwert: ' + Produktwert(10, 2));                                // Output: 20

//#endregion
//#region        Addition
function Summenwert(Summant:number, Summand:number):number{
    return Summant + Summand;
}

                console.info('Function Summenwert: ' + Summenwert(10, 2));                                 // Output: 12
//#endregion
//#region        Substraktion
function Differenzwert(Minuend:number, Subtrahend:number):number{
            return Minuend - Subtrahend;
}

                console.info('Function Differenzwert: ' + Differenzwert(10, 2));                              // Output: 8

//#endregion